package week1.day1;

public class LearnSwitchCondition {
	
	public static void main(String[] args) {
		String browser = "chrome";
		
		switch (browser) {
		case "chrome":System.out.println("open chrome browser");break;
		case "edge":System.out.println("open chrome browser");break;
		case "firefox":System.out.println("open chrome browser");break;
		case "ie":System.out.println("open chrome browser");break;
		default:System.out.println("open opera browser");break;
		}
	}
}
